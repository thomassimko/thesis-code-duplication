public class StringAndROONode
{
   private String string;
   private ROONode node;

   public StringAndROONode(String string, ROONode node)
   {
      this.string = string;
      this.node = node;
   }

   public String getString()
   {
      return this.string;
   }

   public ROONode getNode()
   {
      return this.node;
   }

}
